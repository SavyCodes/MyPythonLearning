# Print Welcome Message
print('Hello World')

# Create a Variable
message = 'Hello World'
print(message)

# To resolve mutiple quote issue 
print("Hello's world")

# Length of Variable
print(len(message))

# Access Index of Characters
print(message[0:5])

# Slicing
print(message[6:])

# All Lower Case, Upper Case
print(message.lower())
print(message.upper())

# Count
print(message.count('Hello'))